package pa4;

//This program uses a for loop that prints
//i, adds 2 to i for each update, and continues
//like this until i=30.
//The output of the program prints every even number from
//2 to 30 on its own line.
public class Even30 {
	public static void main(String[] args) {
		for (int i = 2; i <= 30; i += 2) {
			System.out.println(i);
		}
	}
}
